int g_nCounterRanking;

//=================================================================================================
// --- ランキング関連cppファイルの初期化 ---
//=================================================================================================
void InitRanking(void)
{
	g_nCounterRanking = 0;

	/*** aの初期化 ***/

	/*** ランキングの初期化 ***/
	InitRankingScore();;

	/*** 照明の初期化 ***/
	InitLight();

	/*** カメラの初期化 ***/
	InitCameraRanking();

	/*** ランキングの設定 ***/
	SetRankingScore();

	SetCameraRanking(D3DXVECTOR3(0.0f, 50.0f, 100.0f), D3DXVECTOR3(0.0f, 100.0f, 300.0f), 45.0f, 1, 10000, 300.0f, 1.0f, 0.02f);
}

//=================================================================================================
// --- ランキング関連cppファイルの終了 ---
//=================================================================================================
void UninitRanking(void)
{
	/*** aの終了 ***/

	/*** リザルトスコアの終了 ***/
	UninitRankingScore();

	UninitCameraRanking();

	UninitLight();
}

//=================================================================================================
// --- ランキング関連cppファイルの更新 ---
//=================================================================================================
void UpdateRanking(void)
{
	/*** aの更新 ***/

	/*** ランキングの更新 ***/
	UpdateCameraRanking();

	UpdateLight();

	UpdateRankingScore();

	if ((GetKeyboardTrigger(DIK_RETURN)
		|| GetJoypadTrigger(JOYKEY_A)
		|| GetJoypadTrigger(JOYKEY_START))
		&& GetFade() == FADE_NONE)
	{
		SetFade(MODE_TITLE, FADE_TYPE_NORMAL, 120);

		/*** タイトルBGMをフェードイン ***/
		FadeSound(SOUND_LABEL_BGM_TITLE);
	}

	if (g_nCounterRanking >= 600 && GetFade() == FADE_NONE)
	{
		SetFade(MODE_TITLE, FADE_TYPE_NORMAL);

		/*** タイトルBGMをフェードイン ***/
		FadeSound(SOUND_LABEL_BGM_TITLE);
	}

	g_nCounterRanking++;
}

