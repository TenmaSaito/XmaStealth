{
	/*** aの終了 ***/

	/*** 背景の終了 ***/
	UninitGameOverBg();
}

//=================================================================================================
// --- タイトル関連cppファイルの更新 ---
//=================================================================================================
void UpdateGameOver(void)
{
	/*** aの更新 ***/

	/*** 背景の更新 ***/
	UpdateGameOverBg();

	if ((GetKeyboardTrigger(DIK_RETURN)
		|| GetJoypadTrigger(JOYKEY_A)
		|| GetJoypadTrigger(JOYKEY_START))
		&& GetFade() == FADE_NONE)
	{
		SetFade(MODE_RANKING, FADE_TYPE_NORMAL, 120);
	}

	if (g_nCounterGameOver >= 600 && GetFade() == FADE_NONE)
	{
		SetFade(MODE_RANKING, FADE_TYPE_NORMAL, 120);
	}

	g_nCounterGameOver++;
}

